    private void enrollInClass() {
    int selectedRow = gymClassTable.getSelectedRow();
    if (selectedRow != -1) {
        int classId = (int) gymClassTable.getValueAt(selectedRow, 0);
        try (Connection Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gymba", "root", "")) {
            

            String checkQuery = "SELECT * FROM Enrollments WHERE member_id = ? AND class_id = ?";
            PreparedStatement checkStmt = Con.prepareStatement(checkQuery);
            checkStmt.setInt(1, memberid);  
            checkStmt.setInt(2, classId);
            ResultSet rs = checkStmt.executeQuery();

            if (rs.next()) {

                JOptionPane.showMessageDialog(this, "You are already enrolled in this class!");
            } else {
    
                PreparedStatement enrollStmt = Con.prepareStatement("INSERT INTO Enrollments (member_id, class_id) VALUES (?, ?)");
                enrollStmt.setInt(1, memberid);
                enrollStmt.setInt(2, classId);
                enrollStmt.executeUpdate();


                PreparedStatement updateSpotsStmt = Con.prepareStatement("UPDATE Classes SET available_spots = available_spots - 1 WHERE class_id = ?");
                updateSpotsStmt.setInt(1, classId);
                updateSpotsStmt.executeUpdate();


                JOptionPane.showMessageDialog(this, "Successfully enrolled in the class!");
                ((DefaultTableModel) gymClassTable.getModel()).removeRow(selectedRow); 
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a class to enroll in.");
    }
}
    
