    public ArrayList<User> ListUser(String ValToSearch) {
        ArrayList<User> usersList = new ArrayList<User>();
        
        Statement St;
        ResultSet rs;
        
        try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gymba", "root", "");
            St = Con.createStatement();
            String searchQuery = "select * from `classes` where CONCAT(`class_id`, `class_day`, `class_name`, `start_time`, `end_time`, `instructor`, `available_spots`) LIKE '%" + ValToSearch + "%'";
            rs = St.executeQuery(searchQuery);
            
            User user;
            
            while(rs.next()) {
                user = new User(rs.getInt("class_id"),
                                rs.getString("class_day"),
                                rs.getString("class_name"),
                                rs.getString("start_time"),
                                rs.getString("end_time"),
                                rs.getString("instructor"),
                                rs.getInt("available_spots")
                                );
                usersList.add(user);
            }
        }catch (SQLException ex) {
            ex.printStackTrace();
            Logger.getLogger(UserGymClass.class.getName()).log(Level.SEVERE, null, ex);
    }
        return usersList;
    }
    
    
    
